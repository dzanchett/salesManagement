    private void pagarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarButtonActionPerformed
        // TODO add your handling code here:
        int nParcelas = (int) nParcelasField.getValue();
        double valorParcelas = (double) valorParcelasField.getValue();
        String formaPagamento = formaPagamentoField.getSelectedItem().toString();
        BancoDeDados.pagar(idPedido, nParcelas, valorParcelas, formaPagamento);
        totalPagoVar += (valorParcelas * nParcelas);
        totalAPagarVar -= (valorParcelas * nParcelas);
        totalPagoField.setText("R$ " + String.format("%.2f", totalPagoVar));
        totalAPagarField.setText("R$ " + String.format("%.2f", totalAPagarVar));
        
        if(totalAPagarVar <= 0.0){
            BancoDeDados.setPago(idPedido);
            pagarButton.setEnabled(false);
        }
        
        TelaLogin.atualizaTabelaPedido();
        
        showMessageDialog(null, "Pagamento adicionado.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_pagarButtonActionPerformed

    private void cancelarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelarButtonActionPerformed
        // TODO add your handling code here:
        BancoDeDados.setCancelado(idPedido);
        cancelarButton.setEnabled(false);
        pagarButton.setEnabled(false);
        TelaLogin.atualizaTabelaPedido();
        int nLinhas = dadosCarrinho.getRowCount();
        for(int i = 0; i<nLinhas; i++){
            int qtd = new Integer((String) dadosCarrinho.getValueAt(i, 2));
            int idProduto = new Integer((String) dadosCarrinho.getValueAt(i, 4));
            List<Object> ret = BancoDeDados.getItemFromIdProduto(idProduto);
            int quantidadeEstoque = (int) ret.get(0);
            double preco = (double) ret.get(1);
            BancoDeDados.cadastrarItem(quantidadeEstoque + qtd, preco, idProduto);
        }
        showMessageDialog(null, "Pedido cancelado.", "Atenção", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_cancelarButtonActionPerformed

