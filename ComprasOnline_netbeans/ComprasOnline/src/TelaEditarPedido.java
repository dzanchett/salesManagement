    public TelaEditarPedido(int idPedido, double valorTotal, String status) {
        initComponents();
        setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        pack();
        setLocationRelativeTo(null);
        
        this.idPedido = idPedido;
        
        this.dadosCarrinho = (DefaultTableModel) carrinhoTabela.getModel();
        carrinhoTabela.setAutoCreateRowSorter(true);
        carrinhoTabela.removeColumn(carrinhoTabela.getColumnModel().getColumn(4));
        dadosCarrinho.setRowCount(0);
        
        List<String[]> dadosBanco = BancoDeDados.listarCarrinho(idPedido);
        for(String[] str : dadosBanco){
            dadosCarrinho.addRow(str);
        }
        
        formaPagamentoField.removeAllItems();
        formaPagamentoField.addItem("Cartão de credito");
        formaPagamentoField.addItem("Cartão de debito");
        formaPagamentoField.addItem("Boleto Bancario");
        
        totalPagoVar = BancoDeDados.valorPago(idPedido);
        totalAPagarVar = valorTotal;
        totalAPagarVar -= totalPagoVar;
        
        if(totalAPagarVar <= 0.0){
            pagarButton.setEnabled(false);
        }
        
        totalPagoField.setText("R$ " + String.format("%.2f", totalPagoVar));
        totalAPagarField.setText("R$ " + String.format("%.2f", totalAPagarVar));
        
        if(status.equals("cancelado")){
            cancelarButton.setEnabled(false);
            pagarButton.setEnabled(false);
        }
    }

