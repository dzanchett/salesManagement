    //Mtodo responsvel por atualizar os comboBox de categorias.
    public static void atualizarCategoriaField(){
        mapaCategorias = BancoDeDados.getCategorias();
        Set chaveSet = mapaCategorias.keySet();
        Iterator it = chaveSet.iterator();
        
        categoriaField.removeAllItems();
        categoria2Field.removeAllItems();
        categoria3Field.removeAllItems();

        while(it.hasNext()){
            String item = (String) it.next();
            categoriaField.addItem(item);
            categoria2Field.addItem(item);
            categoria3Field.addItem(item);
        }

        atualizarProdutoField();
    }
    
    //Mtodo responsvel por atualizar os comboBox de produtos.
    public static void atualizarProdutoField(){
        if(produto2Field.isShowing()){
            produto2Field.removeAllItems();
        }
        
        if(produtoField.isShowing()){
            produtoField.removeAllItems();
        }
            
        if(categoria3Field.isShowing() && categoria3Field.getItemCount() > 0 && mapaCategorias.containsKey(categoria3Field.getSelectedItem().toString())){           
            
            int idCategoria3 = (int) mapaCategorias.get(categoria3Field.getSelectedItem().toString());
            mapaProdutos2 = BancoDeDados.getProdutoFromCategoria(idCategoria3);
            Set chaveSet2 = mapaProdutos2.keySet();
            
            Iterator it2 = chaveSet2.iterator();
            
            produto2Field.removeAllItems();

            while(it2.hasNext()){
                String item = (String) it2.next();
                produto2Field.addItem(item);
            }
        }
        
        if(categoria2Field.isShowing() && categoria2Field.getItemCount() > 0 && mapaCategorias.containsKey(categoria2Field.getSelectedItem().toString())){
            
            int idCategoria = (int) mapaCategorias.get(categoria2Field.getSelectedItem().toString());

            mapaProdutos = BancoDeDados.getProdutoFromCategoria(idCategoria);
            Set chaveSet = mapaProdutos.keySet();
            
            Iterator it = chaveSet.iterator();
            
            produtoField.removeAllItems();

            while(it.hasNext()){
                String item = (String) it.next();
                produtoField.addItem(item);
            }
        }
    }
        
