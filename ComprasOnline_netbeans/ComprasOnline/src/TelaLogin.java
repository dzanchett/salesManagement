    private void cadastroFuncionarioCadastrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastroFuncionarioCadastrarButtonActionPerformed
        // TODO add your handling code here:
        String nome = cadastroFuncionarioNomeField.getText();
        String sobrenome = cadastroFuncionarioSobrenomeField.getText();
        String rua = cadastroFuncionarioRuaField.getText();
        String bairro = cadastroFuncionarioBairroField.getText();
        String cidade = cadastroFuncionarioCidadeField.getText();
        String uf = cadastroFuncionarioUFField.getText();
        String cpf = cadastroFuncionarioCpfField.getText();
        String sexo = null;
        if(cadastroFuncionarioMasculinoField.isSelected()){
            sexo = "M";
        }else if(cadastroFuncionarioFemininoField.isSelected()){
            sexo = "F";
        }
        String email = cadastroFuncionarioEmailField.getText();
        char[] senhaTemp = cadastroFuncionarioSenhaField.getPassword();
        String senha = new String("");
        for(char c : senhaTemp){
            senha += c;
        }
        String cargo = cadastroFuncionarioCargoField.getSelectedItem().toString();
        int tipoFuncionario = -1;
        if(cargo.equals("Administrador")){
            tipoFuncionario = BancoDeDados.TIPO_ADMINISTRADOR;
        }else if(cargo.equals("Atendente")){
            tipoFuncionario = BancoDeDados.TIPO_ATENDENTE;
        }

        if(nome.equals("")||sobrenome.equals("")||rua.equals("")||bairro.equals("")||cidade.equals("")||uf.equals("")||cpf.equals("")||sexo==null||email.equals("")||senha.equals("")){
            showMessageDialog(null, "Todos os campos devem ser preenchidos.", "Ateno", JOptionPane.INFORMATION_MESSAGE);
        }else if(!email.contains("@")||!email.contains(".com")){
            showMessageDialog(null, "Formato de e-mail incorreto.\n\nExemplo e-mail:\n\tseuemail@email.com", "Ateno", JOptionPane.INFORMATION_MESSAGE);
        }else if(cpf.length() != 14){
            showMessageDialog(null, "Formato de C.P.F. incorreto.\n\nExemplo C.P.F.:\n\t123.456.789-10", "Ateno", JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                int cadastrado = BancoDeDados.cadastroFuncionario(nome, sobrenome, rua, bairro, cidade, uf, cpf, sexo, email, senha, tipoFuncionario);
                if(cadastrado==0){
                    showMessageDialog(null, "Funcionrio cadastrado com sucesso.", "Ateno", JOptionPane.INFORMATION_MESSAGE);
                    cadastroFuncionarioNomeField.setText("");
                    cadastroFuncionarioSobrenomeField.setText("");
                    cadastroFuncionarioRuaField.setText("");
                    cadastroFuncionarioBairroField.setText("");
                    cadastroFuncionarioCidadeField.setText("");
                    cadastroFuncionarioUFField.setText("");
                    cadastroFuncionarioCpfField.setText("");
                    cadastroFuncionarioFemininoField.setSelected(false);
                    cadastroFuncionarioMasculinoField.setSelected(true);
                    cadastroFuncionarioEmailField.setText("");
                    cadastroFuncionarioSenhaField.setText("");
                    cadastroFuncionarioCargoField.setSelectedIndex(0);
                }else if(cadastrado==1){
                    showMessageDialog(null, "Esse endereo de e-mail j se encontra cadastrado em nosso sistema.", "Ateno", JOptionPane.INFORMATION_MESSAGE);
                }else if(cadastrado==2){
                    showMessageDialog(null, "Esse C.P.F. j se encontra cadastrado em nosso sistema.", "Ateno", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (SQLException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            } catch (NoSuchAlgorithmException ex) {
                Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_cadastroFuncionarioCadastrarButtonActionPerformed

