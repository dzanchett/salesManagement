    private void vendaAdicionarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_vendaAdicionarButtonActionPerformed
        // TODO add your handling code here:
        String produto = vendaProdutoField.getSelectedItem().toString();
        List<Object> ret = BancoDeDados.getItemFromIdProduto((int) mapaProdutos2.get(produto));
        int quantidadeEstoque = (int) ret.get(0);
        double preco = (double) ret.get(1);
        
        int quantidadeCompra = (int) vendaQuantidadeField.getValue();
        
        if(quantidadeCompra > quantidadeEstoque){
            showMessageDialog(null, "Existe apenas " + quantidadeEstoque + " unidades deste produto no estoque.", "Ateno", JOptionPane.INFORMATION_MESSAGE);
        }else{
            BancoDeDados.cadastrarItem(quantidadeEstoque - quantidadeCompra, preco, (int) mapaProdutos2.get(produto));
            String[] str = new String[5];
            str[0] = produto;
            str[1] = vendaCategoriaField.getSelectedItem().toString();
            str[2] = Integer.toString(quantidadeCompra);
            str[3] = Double.toString(preco * quantidadeCompra);
            str[4] = Integer.toString((int) mapaProdutos2.get(produto));
            dadosVendaCarrinho.addRow(str);
            vendaTotalCusto += (preco * quantidadeCompra);
            vendaTotal.setText("R$ " + Double.toString(vendaTotalCusto));
        }
    }//GEN-LAST:event_vendaAdicionarButtonActionPerformed

