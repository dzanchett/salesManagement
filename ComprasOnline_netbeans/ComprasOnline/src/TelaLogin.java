    private void atualizarClienteField(){
        List<String[]> clientes;
        if(tipoUsuarioLogado == BancoDeDados.TIPO_USUARIO_WEB){
            clientes = BancoDeDados.listarClienteFromId(idUsuarioLogado);
        }else{
            clientes = BancoDeDados.listarCliente();
        }

        vendaClientesField.removeAllItems();
        mapaClientes.clear();

        for(String[] str : clientes){
            vendaClientesField.addItem(str[0] + " " + str[1]);
            mapaClientes.put(str[0] + " " + str[1], new Integer(str[9]));
        }
    }
    
